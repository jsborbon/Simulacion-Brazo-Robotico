package estructura;

public class Base {

	private double coord_xBase;
	private double coord_yBase;
	private double coord_zBase;

	public Base() {
		this.coord_xBase = 0;
		this.coord_yBase = 0;
		this.coord_zBase = 0;
	}

	public double getCoord_xBase() {
		return coord_xBase;
	}

	public void setCoord_xBase(double coord_xBase) {
		this.coord_xBase = coord_xBase;
	}

	public double getCoord_yBase() {
		return coord_yBase;
	}

	public double getCoord_zBase() {
		return coord_zBase;
	}

}
